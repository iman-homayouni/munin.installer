clear
echo -e "[>>] ----------------------------------------------------------- [<<]"
echo -e "[>>] Programming and idea by : Iman Homayouni                    [<<]"
echo -e "[>>] Gitbub : https://github.com/iman-homayouni                  [<<]"
echo -e "[>>] Email : homayouni.iman@Gmail.com                            [<<]"
